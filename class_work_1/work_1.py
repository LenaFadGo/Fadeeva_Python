num = int(input("Введите шестизначное число: "))
a = num // 100000
b = num // 10000 % 10
c  = num // 1000 % 10
d = num // 100 % 10
e = num // 10 % 10
f = num % 10
if (a+b+c) == (f+e+d):
    print("Счатливое число")
else:
    print("Не счастливое число")
if 99999 < num < 100000:
    print("Ошибка")

